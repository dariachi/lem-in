#include "lemin.h"

int		are_there_any_ants(int *m, int ant)
{
	int	i;
	int	k;

	i = 0;
	k = 0;
	while (i < ant)
	{
		if (m[i] != 0)
			k = 1;
		i++;
	}
	return (k);
}
